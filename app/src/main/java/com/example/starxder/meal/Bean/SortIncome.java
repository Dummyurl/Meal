package com.example.starxder.meal.Bean;

public class SortIncome {
    //菜品收入
    String dishIncome;
    //酒水收入
    String drinkIncome;
    //大类合计
    String allIncome;
    //出品总数
    String allNum;

    public String getDishIncome() {
        return dishIncome;
    }

    public void setDishIncome(String dishIncome) {
        this.dishIncome = dishIncome;
    }

    public String getDrinkIncome() {
        return drinkIncome;
    }

    public void setDrinkIncome(String drinkIncome) {
        this.drinkIncome = drinkIncome;
    }

    public String getAllIncome() {
        return allIncome;
    }

    public void setAllIncome(String allIncome) {
        this.allIncome = allIncome;
    }

    public String getAllNum() {
        return allNum;
    }

    public void setAllNum(String allNum) {
        this.allNum = allNum;
    }

}
